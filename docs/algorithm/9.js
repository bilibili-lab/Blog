class DoubledNode {
    constructor(element, next, prev) {
        this.element = element;
        this.next = next;
        this.prev = prev;
    }
}

class DoubledLinkList {
    constructor(){
        this.head = null; // 头指针
        this.count = 0;  // 链表的个数
        this.tail = null; // 尾节点
    }
    
}